package lk.ijse.athukorala_hardware.util;

import lk.ijse.athukorala_hardware.db.DBConnection;

import java.sql.Connection;
import java.sql.PreparedStatement;
import java.sql.ResultSet;
import java.sql.SQLException;

public class CrudUtil {
    public static <T> T execute(String sql, Object... obj) throws SQLException {
        Connection conn = DBConnection.getInstance().getConnection();

        PreparedStatement ptsm = conn.prepareStatement(sql);

        for (int i = 0; i < obj.length; i++) {
            ptsm.setObject(i + 1, obj[i]);
        }

        if (sql.startsWith("SELECT") || sql.equals("select")) {
            ResultSet resultSet = ptsm.executeQuery();
            return (T) resultSet;
        } else {
            int result = ptsm.executeUpdate();
            boolean results = result > 0;

            return (T) (Boolean) results;
        }
    }
}
